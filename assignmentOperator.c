//Assignment Operator
#include <stdio.h>
int main()
{
int x = 100;
printf("Value of x is %d\n", x); //100
x += 100;
printf("Value of x is %d\n", x); //200
x -= 100;
printf("Value of x is %d\n", x); //100
x *= 100;
printf("Value of x is %d\n", x); //10000
x /= 100;
printf("Value of x is %d\n", x); //100
return 0;
}
